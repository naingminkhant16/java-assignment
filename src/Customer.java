    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        // TODO add your handling code here:
        try {
            String keyword = txtSearch.getText().trim();
            DBConnection db = new DBConnection();
            
            String sql = "select * from customer where Customer_ID like '%" + keyword + "%' or Customer_Name like '%" + keyword + "%' or Customer_Add like '%"
                    + keyword + "%' or Customer_Tel like '%" + keyword + "%' or Customer_Email like '%"
                    + keyword + "%' or Customer_Gender like '%" + keyword + "%'";
            
            Object[] row = new Object[7];

            db.resultSet = db.statement.executeQuery(sql);

            DefaultTableModel model = (DefaultTableModel) tblCustomer.getModel();
            model.setRowCount(0);

            while (db.resultSet.next()) {
                row[0] = db.resultSet.getString("Customer_ID");
                row[1] = db.resultSet.getString("Customer_Name");
                row[2] = db.resultSet.getString("Customer_Add");
                row[3] = db.resultSet.getString("Customer_Tel");
                row[4] = db.resultSet.getString("Customer_Email");
                row[5] = db.resultSet.getString("Customer_Gender");
                row[6] = db.resultSet.getInt("Customer_Age");
                model.addRow(row);
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex);
        }

    }//GEN-LAST:event_txtSearchKeyReleased
    private boolean emptyFieldsFound() {//true will be return if empty fields are found
        String errMsg = "";

        if (txtId.getText().isEmpty()) {
            txtId.requestFocus();
            errMsg += "ID ";
        }
        if (txtName.getText().isEmpty()) {
            txtName.requestFocus();
            errMsg += "Name ";
        }
        if (txtAddress.getText().isEmpty()) {
            txtAddress.requestFocus();
            errMsg += "Address ";
        }
        if (txtTele.getText().isEmpty()) {
            txtTele.requestFocus();
            errMsg += "Telephone ";
        }
        if (txtEmail.getText().isEmpty()) {
            txtEmail.requestFocus();
            errMsg += "Email ";
        }
        if (txtAge.getText().isEmpty()) {
            txtAge.requestFocus();
            errMsg += "Age ";
        }
        errMsg += "cannot be empty!!";

        boolean emptyFiledFound = (txtId.getText().isEmpty() || txtName.getText().isEmpty() || txtAddress.getText().isEmpty()
                || txtTele.getText().isEmpty() || txtEmail.getText().isEmpty() || txtAge.getText().isEmpty());

        if (emptyFiledFound) {
            JOptionPane.showMessageDialog(this, errMsg);
        }

        return emptyFiledFound;
    }

