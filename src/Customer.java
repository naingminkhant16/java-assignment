    private void tblCustomerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCustomerMouseClicked
        // TODO add your handling code here:        
        DefaultTableModel model = (DefaultTableModel) tblCustomer.getModel();

        int rowIndex = tblCustomer.getSelectedRow();

        txtId.setText(model.getValueAt(rowIndex, 0).toString());
        txtName.setText(model.getValueAt(rowIndex, 1).toString());
        txtAddress.setText(model.getValueAt(rowIndex, 2).toString());
        txtTele.setText(model.getValueAt(rowIndex, 3).toString());
        txtEmail.setText(model.getValueAt(rowIndex, 4).toString());

        if (model.getValueAt(rowIndex, 5).toString().equals("Male")) {
            rdbMale.setSelected(true);
        } else {
            rdbFemale.setSelected(true);
        }

        txtAge.setText(model.getValueAt(rowIndex, 6).toString());

        //disable id text field
        txtId.disable();

        //disable create button
        btnCreate.setEnabled(false);
        //enable update button
        btnUpdate.setEnabled(true);
        //enable delete button
        btnDelete.setEnabled(true);
    }//GEN-LAST:event_tblCustomerMouseClicked

